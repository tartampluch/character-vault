// STATISTIC PIPELINE — The universal stat container
// =============================================================================

/**
 * A fully resolved mathematical pipeline for any statistic.
 *
 * This is the shape of every entry in:
 *   - `Character.attributes`   (STR, DEX, CON, INT, WIS, CHA)
 *   - `Character.combatStats`  (AC normal/touch/flat, BAB, Initiative, Grapple)
 *   - `Character.saves`        (Fortitude, Reflex, Will)
 *   - Any custom homebrew stat (e.g., "stat_sanity", "stat_honour")
 *
 * The engine maintains two separate lists of modifiers:
 *   - `activeModifiers`:      Used to compute `totalBonus` and displayed on the sheet.
 *   - `situationalModifiers`: Never counted in totals; passed to the Dice Engine.
 *
 * SEPARATION OF ACTIVE VS SITUATIONAL:
 *   This is one of the most important architectural decisions in the engine.
 *   Without it, showing "+2 vs Orcs" on the sheet total would be misleading.
 *   With it, the sheet always shows the CHARACTER's true resting value,
 *   and situational bonuses only appear in the dice roll result.
 *
 * DERIVED MODIFIER (`derivedModifier`):
 *   Computed automatically by the engine from `totalValue` using the D&D 3.5 formula:
 *     derivedModifier = Math.floor((totalValue - 10) / 2)
 *   This is meaningful ONLY for the 6 main ability scores. For all other pipelines
 *   (AC, BAB, etc.), this field is 0 and has no functional meaning.
 *   It is NEVER stored in save files — always recomputed from `totalValue`.
 *
 * SETABSOLUTE OVERRIDE:
 *   If any `activeModifier` has `type: "setAbsolute"`, the engine ignores `baseValue`
 *   and all other modifiers. `totalValue` is set directly to the `setAbsolute` value.
 *   `derivedModifier` is still recomputed from this forced value.
 */
export interface StatisticPipeline {
  /**
   * The pipeline's unique identifier.
   * Example: "stat_strength", "combatStats.ac_normal", "saves.fortitude"
   */
  id: ID;

  /**
   * Localized display label shown in the UI.
   * Example: { en: "Strength", fr: "Force" }
   */
  label: LocalizedString;

  /**
   * The base value BEFORE any modifiers are applied.
   *
   * For ability scores: the player-assigned base stat (e.g., 16 = STR 16 before items).
   * For saves/BAB: this is 0; the engine accumulates `base` type modifiers from class
   *               levelProgression entries instead.
   * For custom stats: typically 0 (start from nothing, build up from modifiers).
   *
   * This value IS stored in save files (it represents player choices or GM-set values).
   */
  baseValue: number;

  /**
   * Modifiers that are permanently active or conditionally active at SHEET TIME.
   *
   * The engine populates this list during DAG Phase 0 (flattening) by:
   *   1. Taking all modifiers from all active Features (after prerequisite checks).
   *   2. Evaluating each modifier's `conditionNode` (if present).
   *   3. Filtering OUT modifiers with `situationalContext` (those go to situationalModifiers).
   *
   * The stacking engine (`stackingRules.ts`) then resolves this list to compute `totalBonus`.
   */
  activeModifiers: Modifier[];

  /**
   * Modifiers that are ONLY applicable at DICE ROLL TIME.
   *
   * Populated by the engine from modifiers that have a `situationalContext` field.
   * Never included in `totalBonus`. Passed directly to `parseAndRoll()` for evaluation.
   *
   * The Dice Engine compares each modifier's `situationalContext` string against the
   * provided `RollContext.targetTags` array. If there's a match, the modifier's value
   * is added to `RollResult.situationalBonusApplied`.
   */
  situationalModifiers: Modifier[];

  /**
   * The net bonus from all applicable `activeModifiers`, after stacking rules.
   *
   * Computed by `stackingRules.ts`:
   *   - Same `type` (non-stackable): only the highest value among all modifiers
   *     of that type is included (e.g., two enhancement bonuses → take the larger).
   *   - Stackable types (dodge, circumstance, synergy, untyped): sum all values.
   *   - `multiplier` types: applied AFTER all additive bonuses.
   *   - `setAbsolute` types: override everything; no stacking even considered.
   *
   * Note: Can be negative (Net penalty. e.g., Strength damage from poison).
   */
  totalBonus: number;

  /**
   * The final computed value of this pipeline.
   * Formula: `totalValue = baseValue + totalBonus`
   * Exception: If any modifier has `type: "setAbsolute"`, `totalValue = setAbsolute value`.
   *
   * This is the value displayed on the character sheet as the "final" number.
   */
  totalValue: number;

  /**
   * The D&D 3.5 modifier derived from `totalValue`.
   * Formula: `Math.floor((totalValue - 10) / 2)`
   *
   * Only semantically meaningful for the 6 main ability scores.
   * For all other pipelines, this field is `0` by convention.
   *
   * NOT stored in save files; always recomputed by the engine.
   *
   * Examples:
   *   STR 10 → derivedModifier = 0
   *   STR 18 → derivedModifier = +4
   *   STR 7  → derivedModifier = -2
   *   STR 1  → derivedModifier = -5
   */
  derivedModifier: number;
}

// =============================================================================
// SKILL PIPELINE — Specialised pipeline for skills
// =============================================================================

/**
 * A pipeline specialized for skills, extending `StatisticPipeline`.
 *
 * WHY EXTEND?
 *   Skills share the same additive modifier mechanic as any other stat, but they
 *   have additional D&D 3.5-specific metadata:
 *     - A key ability score (e.g., DEX for Tumble, INT for Knowledge)
 *     - Invested ranks (the player's skill point spending decision)
 *     - Class skill status (affects cost-per-rank and max-rank cap)
 *     - Armour check penalty (for physical skills like Climb, Swim, Hide)
 *     - Trained-only flag (some skills cannot be used without at least 1 rank)
 *
 *   For skills, `baseValue` (from the parent interface) is NOT used for the total
 *   calculation. Instead, the engine uses: `ranks + keyAbilityModifier + totalBonus`.
 *   The `ranks` field replaces `baseValue` as the player-invested component.
 *
 * RANK STORAGE:
 *   `ranks` is the only skill field stored in save files (alongside `baseValue` which
 *   is kept for interface consistency but can be ignored by the skill computation).
 *
 * SYNERGY BONUSES:
 *   Some skills provide synergy bonuses to others when ranks reach 5 (e.g., 5 ranks
 *   in Tumble grants +2 to Balance checks). These are implemented as regular
 *   `Modifier` objects in the Feature's `grantedModifiers`, with a `conditionNode`
 *   like: `{ "@skills.skill_tumble.ranks": { ">=": 5 } }`.
 *   The engine evaluates them like any conditional modifier; no special handling needed.
 */
export interface SkillPipeline extends StatisticPipeline {
  /**
   * The ID of the ability score pipeline that this skill uses as its base modifier.
   * Example: "stat_dexterity" for Tumble, "stat_intelligence" for Knowledge (Arcana).
   *
   * The engine reads `character.attributes[keyAbility].derivedModifier` during
   * DAG Phase 4 to compute the skill's base contribution from the ability score.
   */
  keyAbility: ID;

  /**
   * The number of skill ranks the player has invested in this skill.
   *
   * D&D 3.5 rank costs:
   *   - Class skill:       1 skill point per rank
   *   - Cross-class skill: 2 skill points per rank (max ranks = half of class skill max)
   *
   * Rank caps (per class skill status):
   *   - Class skill:       (character level + 3) maximum ranks
   *   - Cross-class skill: floor((character level + 3) / 2) maximum ranks
   *
   * This value IS stored in save files.
   */
  ranks: number;

  /**
   * Whether this skill counts as a class skill for the character's current class combination.
   *
   * The engine calculates this during DAG Phase 4 by taking the union of all
   * `classSkills` arrays from all active class Features (and domain/racial Features
   * that also declare classSkills). A skill is a class skill if it appears in ANY
   * of the active classes' classSkills arrays.
   *
   * Effect: determines rank cost (1 vs 2 points/rank) and maximum rank cap.
   */
  isClassSkill: boolean;

  /**
   * Whether the armour check penalty applies to this skill.
   *
   * D&D 3.5 armour check penalty affects physical skills like:
   *   Climb, Jump, Swim, Hide, Move Silently, Balance, Tumble, Escape Artist, etc.
   * It does NOT affect mental skills like Spellcraft, Knowledge (all), Diplomacy, etc.
   *
   * The penalty value is computed during DAG Phase 3 (as a combat stat pipeline)
   * and injected as a modifier into affected skills during Phase 4.
   */
  appliesArmorCheckPenalty: boolean;

  /**
   * Whether this skill can be used without any invested ranks.
   *
   * Most skills can be attempted untrained (e.g., Climb, Spot, Listen).
   * Some skills explicitly require training (e.g., Spellcraft, Disable Device,
   * Knowledge (all), Perform, and many psionic skills).
   * When `false`, the engine should warn the UI when ranks = 0.
   */
  canBeUsedUntrained: boolean;
}

// =============================================================================
// RESOURCE POOL — Depletable resources (HP, psi points, charges, etc.)
// =============================================================================

/**
 * A resource that can be depleted and restored.
 *
 * Design:
 *   Resource pools are intentionally separate from stat pipelines because they
 *   have a CURRENT VALUE that changes during play (combat rounds, spell use, etc.).
 *   A `StatisticPipeline` tracks a character's capabilities; a `ResourcePool` tracks
 *   their remaining capacity to exercise those capabilities.
 *
 * Examples of resource pools:
 *   - `resources.hp`           : Hit Points (current, temporary, max)
 *   - `resources.psi_points`   : Psionic power points (Psion, Wilder, etc.)
 *   - `resources.rage_rounds`  : Barbarian Rage rounds per day
 *   - `resources.turn_undead`  : Cleric Turn Undead uses per day
 *   - `resources.item_charges` : Charges on a wand, staff, or ring
 *
 * MAXIMUM VALUE VIA PIPELINE:
 *   The maximum is not stored directly — it is computed by a separate `StatisticPipeline`
 *   referenced by `maxPipelineId`. This allows the max to be dynamic:
 *     - HP max depends on CON modifier, which changes with Belt of Constitution.
 *     - Psi Points max depends on manifester level and key ability.
 *   When the referenced pipeline updates, the resource pool's effective max auto-updates
 *   because the DAG reads `maxPipelineId` to find the max at display time.
 *
 * TEMPORARY VALUES:
 *   `temporaryValue` is used for buffs like Temporary HP (from Aid Another, Heroism KO'd check, etc.).
 *   D&D 3.5 rule: temporary HP absorbs damage FIRST, before regular HP is reduced.
 *   The UI must implement this correctly (Phase 10.1).
 *
 * HOW STORED:
 *   `currentValue` and `temporaryValue` ARE stored in save files (they represent
 *   in-play state). `maxPipelineId` is stored as a pointer ID. The actual max is
 *   recomputed from the pipeline at load time.
 */
export interface ResourcePool {
  /**
   * Unique identifier for this resource pool.
   * Convention: "resources.<name>" (e.g., "resources.hp", "resources.psi_points")
   */
  id: ID;

  /**
   * Localized display label.
   * Example: { en: "Hit Points", fr: "Points de vie" }
   */
  label: LocalizedString;

  /**
   * The ID of the `StatisticPipeline` that computes this resource's MAXIMUM value.
   *
   * This pipeline is defined and computed in DAG Phase 3. For HP:
   *   "stat_max_hp" pipeline formula:
   *   sum of (hitDieResult[level] for each level) + (CON.derivedModifier × character level)
   *
   * The ResourcePool reads this pipeline's `totalValue` at display time to know the cap.
   */
  maxPipelineId: ID;

  /**
   * The character's current value of this resource.
   * Starts at the computed max on character creation or after a long rest.
   * Updated by the player (and by the GM via overrides) during play.
   *
   * For HP: Decreases when taking damage, increases when healed.
   * Note: current HP can go negative (unconscious/dying in D&D 3.5).
   */
  currentValue: number;

  /**
   * Temporary bonus value that absorbs depletion before `currentValue` is affected.
   *
   * D&D 3.5 rule for temporary HP:
   *   When taking damage, temporary HP is reduced FIRST.
   *   Temporary HP does not stack with itself (new temporary HP replaces old, unless
   *   the old is higher — per SRD). The engine/UI should enforce this rule.
   *
   * For psi points and other non-HP resources: temporarily restored capacity
   * (e.g., a partial restore from a psionic power). Usually 0.
   */
  temporaryValue: number;

  /**
   * The condition under which this resource naturally resets (or incrementally recharges).
   *
   * ─── FULL RESET (restore to maximum) ───────────────────────────────────────
 * - `"long_rest"`:  D&D 3.5 "8 hours of restful sleep" (SRD standard for spells, HP).
 * - `"short_rest"`: Optional house rule (not default D&D 3.5 — typically for variants).
 * - `"encounter"`:  Resets at the start of each combat encounter (some class abilities).
 *                   Triggered by `GameEngine.triggerEncounterReset()`.
 * - `"never"`:      Does not reset automatically (e.g., finite item charges like Ring of
 *                   the Ram's 50 charges, Ring of Three Wishes gems, XP-spent powers).
 *                   Requires an explicit restore action or a GM override.
 *
 * ─── CALENDAR RESET (restore to maximum at a fixed in-game time boundary) ───
 * - `"per_day"`:    Resets at DAWN each in-game day, regardless of whether the character
 *                   slept. This is distinct from `"long_rest"` — a character who stays
 *                   awake all night still sees dawn and has their X/day charges reset.
 *                   Triggered by `GameEngine.triggerDawnReset()`.
 *                   Used for: X/day ring abilities (Ring of Djinni Calling 1/day, Ring of
 *                   Spell Turning 3/day), X/day item charges, class features stated as
 *                   "once per day" that reset independently of rest (e.g., some activated
 *                   item abilities on the Elemental Command rings).
 *
 * - `"per_week"`:   Resets once per in-game WEEK, typically at the GM's discretion.
 *                   Triggered by `GameEngine.triggerWeeklyReset()`.
 *                   Used for: X/week ring abilities (Elemental Command rings:
 *                   chain lightning 1/week, ice storm 2/week, etc.), rare X/week item
 *                   powers added by magic item tables in the SRD.
 *
 * D&D 3.5 DESIGN RATIONALE — "per_day" vs "long_rest":
 *   The SRD consistently differentiates between abilities that reset "per day" (calendar)
 *   and resources that require "8 hours of restful sleep" to recover. A Ring of Djinni
 *   Calling can be used once per day at midnight+1 ms, whether or not the wearer slept.
 *   Spell slots, however, explicitly need sleep. By separating these into two conditions,
 *   the engine correctly handles adventuring parties that skip rest while still allowing
 *   daily-reset items to function on their calendar schedule.
   *
   * ─── INCREMENTAL RECHARGE (add `rechargeAmount` per tick, capped at max) ──
   * - `"per_turn"`:   Recharges at the START OF THE CHARACTER'S OWN TURN each round.
   *                   Triggered by `GameEngine.triggerTurnTick()`.
   *                   Used for: Fast Healing (regain X HP per turn), Regeneration (regrow to
   *                   max HP per turn — same tick, different bypass rules tracked separately),
   *                   any ability that recovers gradually each time the character acts.
   *
   * - `"per_round"`:  Recharges at a FIXED POINT once per combat round (e.g., start of the
   *                   *global* round, regardless of initiative order).
   *                   Triggered by `GameEngine.triggerRoundTick()`.
   *                   Used for: area-of-effect sustained damage pools (e.g., an environmental
   *                   hazard that deals X per round), aura charges that accumulate each round.
   *
   * DESIGN NOTE — WHY TWO INCREMENTAL VARIANTS:
   *   `"per_turn"` and `"per_round"` are separate because D&D 3.5 distinguishes between
   *   "at the start of your turn" (character-specific initiative) and "once per round"
   *   (global initiative clock). Fast Healing is always "at the start of YOUR turn".
   *   Future aura damage or timed conditions may need "once per round" independent of who acts.
   *
   * ENGINE CONTRACT:
   *   For `"per_turn"` and `"per_round"`, the engine calls `triggerTurnTick()` /
   *   `triggerRoundTick()` on the ACTIVE character's `GameEngine` instance. It is the
   *   UI / game loop's responsibility to call these methods at the correct time.
   *   The engine does NOT auto-call these — it is stateless w.r.t. the combat round clock.
   *
   * `rechargeAmount` controls HOW MUCH is restored per tick (see field below).
   *
   * @see GameEngine.triggerTurnTick()     — call at the start of the character's turn
   * @see GameEngine.triggerRoundTick()    — call once per global round
   * @see GameEngine.triggerEncounterReset() — call at the start of a new encounter
   * @see ARCHITECTURE.md section 4 (ResourcePool) for full documentation
   */
  resetCondition:
    | 'short_rest'
    | 'long_rest'
    | 'encounter'
    | 'never'
    | 'per_turn'
    | 'per_round'
    | 'per_day'   // Calendar dawn reset — X/day item charges, independent of sleep
    | 'per_week'; // Calendar weekly reset — X/week item abilities

  /**
   * Amount restored per tick for `"per_turn"` and `"per_round"` reset conditions.
   *
   * Ignored for all other `resetCondition` values (full-reset conditions always
   * restore to the computed maximum from `maxPipelineId`).
   *
   * D&D 3.5 EXAMPLES:
   *  - Fast Healing 3: `resetCondition: "per_turn"`, `rechargeAmount: 3`
   *    — At the start of each of the creature's turns, it regains 3 HP.
   *    — The creature still dies at −10 HP; Fast Healing stops working at 0 HP (nonlethal).
   *  - Regeneration 5: `resetCondition: "per_turn"`, `rechargeAmount: 5`
   *    — Same tick as Fast Healing but upgrades lethal → nonlethal (tracked via tags).
   *    — The `rechargeAmount` is the same; which damage is lethal vs bypassed is a
   *      separate concern handled by DR/bypass tags on the creature Feature.
   *
   * FORMULA OR NUMBER:
   *   Can be a plain number (most common) OR a Math Parser formula string, enabling
   *   scaling healing rates:
   *     `"floor(@classLevels.class_cleric / 2)"` — scales with cleric level
   *     `"@attributes.stat_constitution.derivedModifier"` — scales with CON modifier (unusual but valid)
   *
   * DEFAULT: `undefined` / `0` — no incremental recharge. Pools with full-reset
   * `resetCondition` values can omit this field.
   *
   * CAPPING RULE (enforced by tick methods):
   *   After applying `rechargeAmount`, `currentValue` is capped at the effective maximum
   *   (`maxPipelineId` pipeline's `totalValue`). It can never exceed its maximum via ticking.
   *   Temporary HP is NOT affected by tick recharges — `temporaryValue` is unchanged.
   */
  rechargeAmount?: number | string;
}
