/**
 * @file pipeline.ts
 * @description Mathematical pipeline types: Modifiers, StatisticPipeline, SkillPipeline,
 *              and ResourcePool.
 *
 * Design philosophy:
 *   EVERYTHING that can be calculated is a Pipeline. Strength, Armour Class, Initiative,
 *   Land Speed, Jump modifier, current HP — all are pipelines. This uniformity means:
 *     1. The engine has one code path for any stat: sum active modifiers, apply stacking rules.
 *     2. Any modifier can target any pipeline by its `ID`, including future homebrew stats.
 *     3. The UI always reads the same shape of data.
 *
 *   THE THREE-PHASE MODIFIER LIFECYCLE:
 *     1. PASSIVE (activeModifiers):
 *        Modifiers that are always "on" when the feature is active (e.g., +2 STR from a Belt).
 *        These feed into `totalBonus` and `totalValue` shown on the character sheet.
 *
 *     2. CONDITIONAL (activeModifiers with `conditionNode`):
 *        Modifiers that are only active when a runtime condition is met.
 *        Example: Monk AC bonus (WIS mod to AC, but only without armour/shield/load).
 *        These are evaluated at sheet-computation time (every time the DAG reruns).
 *        If the condition is TRUE → they join `activeModifiers` totals.
 *        If the condition is FALSE → they are ignored.
 *
 *     3. SITUATIONAL (situationalModifiers with `situationalContext`):
 *        Modifiers that only apply at DICE ROLL TIME, against a specific target context.
 *        Example: Ranger's Favoured Enemy (+2 attack/damage vs Orcs).
 *        These are NEVER added to `totalBonus` on the sheet.
 *        The Dice Engine reads `situationalModifiers`, matches `situationalContext`
 *        against the roll's `RollContext.targetTags`, and adds matched bonuses to
 *        `RollResult.situationalBonusApplied`.
 *
 * @see src/lib/types/primitives.ts  for ID, ModifierType
 * @see src/lib/types/i18n.ts        for LocalizedString
 * @see src/lib/types/logic.ts       for LogicNode
 * @see src/lib/utils/stackingRules.ts (Phase 2.4) for stacking resolution
 * @see src/lib/utils/diceEngine.ts    (Phase 2.5) for situational bonus evaluation
 */

import type { ID, ModifierType } from './primitives';
import type { LocalizedString } from './i18n';
import type { LogicNode } from './logic';

// =============================================================================
// MODIFIER — The atomic unit of change
// =============================================================================

/**
 * A single modifier that adjusts a pipeline's value.
 *
 * Modifiers are ALWAYS sourced from a Feature (race, class, feat, item, spell, etc.).
 * They are never generated by the engine itself — the engine only reads and aggregates them.
 *
 * KEY FIELD: `situationalContext`
 *   If this field is present, the modifier is SITUATIONAL and is NEVER counted in
 *   `totalBonus`. It lives in `StatisticPipeline.situationalModifiers` instead.
 *   This is the core mechanism for Favoured Enemy, flanking bonuses, etc.
 *
 * KEY FIELD: `conditionNode`
 *   If this field is present, the modifier is CONDITIONAL. The engine evaluates
 *   the node at sheet-computation time. Only if the condition is TRUE does the
 *   modifier count toward `totalBonus`.
 *
 * KEY FIELD: `value`
 *   Can be a number (most common) OR a formula string (e.g., "@attributes.stat_wisdom.derivedModifier").
 *   String values are resolved by the Math Parser at computation time.
 *   This enables data-driven cross-stat dependencies:
 *     - Monk WIS → AC
 *     - Paladin CHA → saves
 *     - Psionic manifester level formulas
 *
 * @example Flat numerical modifier (Ring of Protection +2):
 * ```json
 * {
 *   "id": "mod_ring_protection_ac",
 *   "sourceId": "item_ring_of_protection_2",
 *   "sourceName": { "en": "Ring of Protection +2" },
 *   "targetId": "combatStats.ac_normal",
 *   "value": 2,
 *   "type": "deflection"
 * }
 * ```
 *
 * @example Formula-as-value modifier (Monk WIS to AC):
 * ```json
 * {
 *   "id": "mod_monk_wis_ac",
 *   "sourceId": "class_feature_monk_ac_bonus",
 *   "sourceName": { "en": "Monk Wisdom AC", "fr": "CA Sagesse Moine" },
 *   "targetId": "combatStats.ac_normal",
 *   "value": "@attributes.stat_wisdom.derivedModifier",
 *   "type": "untyped",
 *   "conditionNode": { ... }
 * }
 * ```
 *
 * @example Situational modifier (Favoured Enemy: Orcs):
 * ```json
 * {
 *   "id": "mod_fe_orc_attack",
 *   "sourceId": "class_feature_ranger_favoured_enemy",
 *   "sourceName": { "en": "Favoured Enemy: Orc" },
 *   "targetId": "combatStats.attack_bonus",
 *   "value": 2,
 *   "type": "untyped",
 *   "situationalContext": "orc"
 * }
 * ```
 */
export interface Modifier {
  /**
   * Unique identifier for this specific modifier instance within its source Feature.
   * Used by the stacking engine to identify duplicates and by the UI breakdown modal.
   */
  id: ID;

  /**
   * The ID of the Feature that grants this modifier.
   * Used for UI attribution ("This +2 comes from your Ring of Protection").
   */
  sourceId: ID;

  /**
   * The localized display name of the source, shown in the breakdown modal.
   * Example: { en: "Ring of Protection +2", fr: "Anneau de protection +2" }
   */
  sourceName: LocalizedString;

  /**
   * The ID of the pipeline this modifier targets.
   *
   * CONVENTION:
   *   - Main attributes:  "stat_strength", "stat_dexterity", "stat_constitution", "stat_intelligence", "stat_wisdom", "stat_charisma"
   *   - Combat stats:     "combatStats.ac_normal", "combatStats.base_attack_bonus", "combatStats.initiative"
   *   - Saves:            "saves.fortitude", "saves.reflex", "saves.will"
   *   - Skills:           "skills.skill_climb", "skills.skill_jump"
   *   - Resources:        "resources.hp.maxValue" (via the maxPipelineId pointer)
   *   - Custom homebrew:  Any ID — the engine auto-creates the pipeline on first encounter.
   *
   * SPECIAL PREFIX — `"attacker.*"` (Enhancement E-5):
   *   Modifiers whose `targetId` begins with `"attacker."` are attacker modifiers.
   *   They impose penalties on incoming attackers rather than on the modifier owner.
   *
   *   These modifiers are NEVER included in the owner's static pipeline totals.
   *   They are collected separately from the DAG and applied at Dice Engine roll time,
   *   targeting the attacker's roll (not the defender's sheet).
   *
   *   The `"attacker."` prefix is stripped at resolution time — the remainder is treated
   *   as a pipeline path on the attacker's context.
   *
   *   Example: `"attacker.combatStats.attack_bonus"` with `value: -1` and
   *   `situationalContext: "vs_air_elementals"` → the attacking air elemental
   *   takes −1 to their attack roll.
   *
   *   @see ARCHITECTURE.md section 4.6 — `attacker.*` target namespace full documentation
   */
  targetId: ID;

  /**
   * The bonus or penalty value.
   *
   * - `number`: Applied directly (positive = bonus, negative = penalty).
   * - `string`: A Math Parser formula resolved at computation time.
   *             Must be a valid math expression with optional `@`-path variables.
   *             Examples:
   *               "@attributes.stat_wisdom.derivedModifier"   (Paladin/Monk cross-stat)
   *               "floor(@classLevels.class_soulknife / 4)" (scaling ability formula)
   *               "2 * @attributes.stat_strength.derivedModifier" (two-handed damage)
   */
  value: number | string;

  /**
   * The D&D 3.5 modifier type, governing stacking behaviour.
   * See `ModifierType` in primitives.ts for full documentation.
   */
  type: ModifierType;

  /**
   * Optional: A logic tree evaluated at SHEET COMPUTATION TIME.
   * If present and evaluates to `false`, this modifier is completely ignored
   * and does not contribute to `totalBonus`.
   *
   * Examples of conditional modifiers:
   *   - Monk AC Bonus: only active when not wearing armour/shield/heavy load.
   *   - Rage Bonus: only active when the Barbarian is raging (has "raging" tag).
   *   - Shield Bonus from Mage Armor: only active if no physical shield equipped.
   *
   * If absent, the modifier is always active (when the parent Feature is active).
   */
  conditionNode?: LogicNode;

  /**
   * Optional: A context tag string for SITUATIONAL modifiers.
   *
   * WHY THIS DESIGN:
   *   Situational modifiers (like Favoured Enemy) must NOT appear on the static
   *   character sheet total — the character's attack bonus vs Orcs is NOT their
   *   general attack bonus. But the modifier must be available at dice-roll time.
   *
   *   Solution: if `situationalContext` is present, the engine routes this modifier
   *   to `StatisticPipeline.situationalModifiers` instead of `activeModifiers`.
   *   The Dice Engine then matches `situationalContext` against the roll's
   *   `RollContext.targetTags` to decide if it applies.
   *
   * Convention: use the same string as a tag that would appear in `@targetTags`.
   * Examples: "orc", "goblinoid", "attack_of_opportunity", "flanking", "evil"
   *
   * DUAL-GATING (BOTH conditionNode AND situationalContext may coexist):
   *   A modifier CAN have BOTH fields simultaneously. The engine handles this in two stages:
   *
   *   Stage 1 (SHEET TIME — `conditionNode` evaluation in Phase 0):
   *     The `conditionNode` is evaluated against the character's current state.
   *     If it FAILS: the modifier is completely ignored (not even added to situationalModifiers).
   *     If it PASSES: proceed to Stage 2.
   *
   *   Stage 2 (ROLL TIME — `situationalContext` matching):
   *     Because `situationalContext` is present, the modifier goes to `situationalModifiers`
   *     (not `activeModifiers`). It is only applied when the roll context includes the tag.
   *
   *   CANONICAL EXAMPLE — Barbarian Indomitable Will (ANNEXES.md section A.1.1):
   *     "+4 Will save vs Enchantment spells while Raging"
   *     - `conditionNode`: must have tag "rage" (sheet-time gating — only while raging)
   *     - `situationalContext`: "vs_enchantment" (roll-time gating — only vs enchantment)
   *     Both conditions must be met: the Barbarian must be RAGING and the roll must be
   *     AGAINST AN ENCHANTMENT for the +4 bonus to apply.
   */
  situationalContext?: string;

  /**
   * Optional: Material or condition tags that BYPASS this Damage Reduction entry.
   *
   * D&D 3.5 CONTEXT — DAMAGE REDUCTION (SRD "Special Abilities", dr section):
   *   DR is written as "DR X/material" (e.g., DR 5/silver, DR 10/magic, DR 15/adamantine).
   *   The number X is the modifier `value`; the bypass material is `drBypassTags`.
   *
   *   If an attack is made with a weapon that carries ANY tag matching an entry in this
   *   array, the DR does NOT reduce the damage for that hit (the bypass "works").
   *
   *   EXAMPLES:
   *   ```json
   *   // Barbarian DR 1/— (overcome by nothing — empty array = "—")
   *   { "value": 1, "type": "damage_reduction", "drBypassTags": [] }
   *
   *   // Vampire DR 10/magic (any +1 or better magic weapon bypasses)
   *   { "value": 10, "type": "damage_reduction", "drBypassTags": ["magic"] }
   *
   *   // Lycanthrope DR 10/silver (silver weapon bypasses)
   *   { "value": 10, "type": "damage_reduction", "drBypassTags": ["silver"] }
   *
   *   // Devil DR 10/good (good-aligned weapon bypasses)
   *   { "value": 10, "type": "damage_reduction", "drBypassTags": ["good"] }
   *
   *   // Demon DR 10/cold_iron (cold iron weapon bypasses — same as silver rule)
   *   { "value": 10, "type": "damage_reduction", "drBypassTags": ["cold_iron"] }
   *
   *   // Epic creature DR 10/epic (bypassed only by +6 or better weapons)
   *   { "value": 10, "type": "damage_reduction", "drBypassTags": ["epic"] }
   *
   *   // AND bypass (must be BOTH magic AND silver — extremely rare)
   *   { "value": 15, "type": "damage_reduction", "drBypassTags": ["magic", "silver"] }
   *   ```
   *
   * STACKING RULE FOR DR (implemented in `stackingRules.ts`):
   *   DR does NOT add across sources: "A creature does not get a better DR by
   *   combining both its DR 5/magic and its DR 10/silver — it simply has both."
   *   (SRD Special Abilities).
   *   When multiple DR modifiers target `combatStats.damage_reduction`:
   *   - They are grouped BY their `drBypassTags` signature (JSON-serialised array).
   *   - Within each bypass group, ONLY THE HIGHEST value applies (no additive stacking).
   *   - Each bypass group contributes independently as a separate DR entry.
   *   The UI displays this as "DR 5/magic AND DR 10/silver" (both listed separately).
   *
   *   Exception — SAME SOURCE STACKS: When the Barbarian gains DR/— from multiple
   *   class-level milestones, THOSE DO stack because they all share the same empty
   *   bypass signature (`[]`) AND are accumulated from a single class progression.
   *   The engine handles this by using `type: "base"` for same-source progression
   *   increments (which stack per the ALWAYS_STACKING_TYPES rule in stackingRules.ts)
   *   AND `type: "damage_reduction"` for cross-source DR comparisons.
   *
   *   CONTENT AUTHORING GUIDE:
   *   - For class-progression DR that ADD together (Barbarian DR/—):
   *     Use `type: "base"` targeting `combatStats.damage_reduction` WITHOUT `drBypassTags`.
   *     The ALWAYS_STACKING_TYPES rule will sum them correctly.
   *   - For racial/template/innate DR that should use "best wins" (not additive):
   *     Use `type: "damage_reduction"` WITH `drBypassTags` set appropriately.
   *     This triggers the special DR resolution in `applyStackingRules`.
   *
   * UI RENDERING:
   *   The character sheet reads `combatStats.damage_reduction.activeModifiers`,
   *   groups them by `drBypassTags` signature, and displays each DR entry as a
   *   formatted string: "DR 5/magic, DR 10/silver". If `drBypassTags` is empty,
   *   it renders as "DR X/—" (overcome by nothing).
   *
   * COMBAT APPLICATION (Dice Engine):
   *   At roll time, the Dice Engine reads `drEntries` (see `StackingResult`) from
   *   the `combatStats.damage_reduction` pipeline breakdown. For each DR entry, it
   *   checks whether the attacking weapon's tags include ANY bypass tag. If yes: the
   *   DR is overcome and no reduction is applied. If no: subtract the DR value from
   *   the damage total (minimum 0 damage per hit, except for spells and touch attacks).
   *
   * Only meaningful when `type === "damage_reduction"`.
   * Ignored for all other modifier types.
   *
   * @see stackingRules.ts — `applyStackingRules()` DR grouping + best-wins logic
   * @see ARCHITECTURE.md section 4.5 — Damage Reduction full reference
   */
  drBypassTags?: string[];
}

